void PrintImage(int[,] image) // по исходной матрице печатаем контур
{
    for (int i = 0; i < image.GetLength(0); i++)
    {
        for (int j = 0; j < image.GetLength(1); j++)
        {
            if (image[i, j] == 0) //если в значение ячейки 0,
                Console.Write($" "); // то вместо нуля печатаем пробел
            else Console.Write($"+");
        }
        Console.WriteLine();
    }
}

// Этод метод закрашивания подходит только для картинок с замкнутым контуром

void FillImage(int row, int col) //закрашивание картинку
{
    if (pic[row, col] == 0) //закрашиваем только ячейки с нулями
    {
        pic[row, col] = 1;
        FillImage(row - 1, col); //метод вызывает сам себя (рекурсия)
        FillImage(row, col - 1); 
        FillImage(row + 1, col);
        FillImage(row, col + 1);
    }
}
